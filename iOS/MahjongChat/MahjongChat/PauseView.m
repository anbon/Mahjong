#import "PauseView.h"

@implementation PauseView
@synthesize  window, background, spinner, image, label;
//-------------------------------------------------------------------------------------------------------------------------------------------------
+(PauseView *)pauseView
//-------------------------------------------------------------------------------------------------------------------------------------------------
{
    NSLog(@"123");
    
    static PauseView *pauseView;
    static dispatch_once_t onceToken;
    dispatch_once(&onceToken, ^{
        pauseView = [[PauseView alloc]init];
    });
    return pauseView;
}
//-------------------------------------------------------------------------------------------------------------------------------------------------
-(void)pauseViewShow:(NSString *)status
//-------------------------------------------------------------------------------------------------------------------------------------------------
{
    
}
//-------------------------------------------------------------------------------------------------------------------------------------------------
-(void)dismissPauseView
//-------------------------------------------------------------------------------------------------------------------------------------------------
{
    
}
//-------------------------------------------------------------------------------------------------------------------------------------------------
-(id)init
//-------------------------------------------------------------------------------------------------------------------------------------------------
{
    self = [super initWithFrame:[[UIScreen mainScreen] bounds]];
    //---------------------------------------------------------------------------------------------------------------------------------------------
    id<UIApplicationDelegate> delegate = [[UIApplication sharedApplication] delegate];
    //---------------------------------------------------------------------------------------------------------------------------------------------
    if ([delegate respondsToSelector:@selector(window)])
        window = [delegate performSelector:@selector(window)];
    else window = [[UIApplication sharedApplication] keyWindow];
    //---------------------------------------------------------------------------------------------------------------------------------------------
    background = nil; spinner = nil; image = nil; label = nil;
    //---------------------------------------------------------------------------------------------------------------------------------------------
    self.alpha = 0;
    //---------------------------------------------------------------------------------------------------------------------------------------------
    return self;
}

@end
